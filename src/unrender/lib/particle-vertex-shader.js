module.exports = [
'attribute float size;',
'attribute vec4 customColor;',
'',
'varying vec4 vColor;',
'varying float vPointSize;',
'',
'void main() {',
'  vColor = customColor/255.0;',
'  vec4 mvPosition = modelViewMatrix * vec4( position, 1.0 );',
'',
'  // Cull points behind the camera (mvPosition.z > 0 in view space = behind)',
'  if (mvPosition.z > 0.0) {',
'    gl_Position = vec4(2.0, 2.0, 2.0, 1.0);',
'    gl_PointSize = 0.0;',
'    return;',
'  }',
'',
'  vPointSize = size * ( 351.0 / length( mvPosition.xyz ) );',
'',
'  // Cull sub-pixel points: avoids fragment shader invocation for very distant nodes',
'  if (vPointSize < 0.05) {',
'    gl_Position = vec4(2.0, 2.0, 2.0, 1.0);',
'    gl_PointSize = 0.0;',
'    return;',
'  }',
'',
'  gl_PointSize = max(vPointSize, 1.0);',
'  gl_Position = projectionMatrix * mvPosition;',
'}'
].join('\n');
